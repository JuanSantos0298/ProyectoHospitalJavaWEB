package Modelo;

/**
 *
 * @author alex4
 */
public class Paciente {
    int idPaciente;
    String nombre;
    String apellidoPaterno;
    String apellidoMaterno;
    String telefono;
    String direccion;
    String seguroMedico;
    int edad;
    String estadoCivil;
    int medicoAsignado;

    public Paciente() {
    }

    public Paciente(int idPaciente, String nombre, String apellidoPaterno, String apellidoMaterno, String telefono, String direccion, String seguroMedico, int edad, String estadoCivil) {
        this.idPaciente = idPaciente;
        this.nombre = nombre;
        this.apellidoPaterno = apellidoPaterno;
        this.apellidoMaterno = apellidoMaterno;
        this.telefono = telefono;
        this.direccion = direccion;
        this.seguroMedico = seguroMedico;
        this.edad = edad;
        this.estadoCivil = estadoCivil;
    }

    public int getIdPaciente() {
        return idPaciente;
    }

    public void setIdPaciente(int idPaciente) {
        this.idPaciente = idPaciente;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellidoPaterno() {
        return apellidoPaterno;
    }

    public void setApellidoPaterno(String apellidoPaterno) {
        this.apellidoPaterno = apellidoPaterno;
    }

    public String getApellidoMaterno() {
        return apellidoMaterno;
    }

    public void setApellidoMaterno(String apellidoMaterno) {
        this.apellidoMaterno = apellidoMaterno;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getSeguroMedico() {
        return seguroMedico;
    }

    public void setSeguroMedico(String seguroMedico) {
        this.seguroMedico = seguroMedico;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public String getEstadoCivil() {
        return estadoCivil;
    }

    public void setEstadoCivil(String estadoCivil) {
        this.estadoCivil = estadoCivil;
    }

    public int getMedicoAsignado() {
        return medicoAsignado;
    }

    public void setMedicoAsignado(int medicoAsignado) {
        this.medicoAsignado = medicoAsignado;
    }
    
    
    
}
